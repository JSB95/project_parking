package JSB;



public class Count {
	
	// 필드
	private int year, month, day;
	
	// 생성자
	public Count() {
	}

	public Count(int year, int month, int day) {
		this.year = year;
		this.month = month;
		this.day = day;
	}

	
	// 메소드
	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}
	


}
